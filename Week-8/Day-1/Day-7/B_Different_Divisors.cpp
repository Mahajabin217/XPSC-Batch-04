bool prime(ll n)
{
    if (n < 2)
        return false;
    if (n <= 3)
        return true;
    if (n % 2 == 0)
        return false;
    for (ll i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}
void solve()
{
    ll n;
    cin >> n;
    ll x = n + 1;
    while (!prime(x))
    {
        x++;
    }
    ll y = x + n;
    while (!prime(y))
    {
        y++;
    }
    ll ans = x * y;
    cout << ans << endl;
}